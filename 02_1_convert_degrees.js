/* A JavaScript program to convert degrees Celsius to degrees Fahrenheit and write
the result to the page in a descriptive sentence.
Equation for Fahrenheit to Celsius: fahrenheit = 9 / 5 * celsius + 32 */

var celsius = prompt("What is the degrees in celsius?", "");
var fahrenheit = 9 / 5 * celsius + 32;

alert(celsius + "\xB0 Celsius is " + fahrenheit + "\xB0 Fahrenheit.");
